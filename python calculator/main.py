choice = input("enter operators")
x = int(input("enter first number "))
y = int(input("enter second number "))
if choice == "+":
    result = (x+y)
    print(result)
if choice == "-":
    result = (x-y)
    print(result)
if choice == "*":
    result = (x*y)
    print(result)
if choice == "/":
    result = (x/y)
    print(result)

